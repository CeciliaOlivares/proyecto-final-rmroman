# Calcula velocidad promedio, dado el tiempo y la distancia

distancia = int(input("Teclear distancia de tu viaje en km enteros: "))
tiempo = int(input("Teclear tiempo de tu viaje en hrs enteros: "))

velocidad = distancia/tiempo

print ("velocidad promedio: ", velocidad, "km/h")
